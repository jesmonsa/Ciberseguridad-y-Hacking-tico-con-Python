# Función con parámetros
def mi_funcion2(arg1, arg2):
    print(arg1 + arg2)

mi_funcion2(1, 2)

# Función con retorno
def mi_funcion3(arg1, arg2):
    return arg1 + arg2

resultado = mi_funcion3(1, 2)
print(resultado)


